      <p>5、请按参赛办法要求，严格控制集体创作的参赛者人数：</p>
      <p>（1）每件作品的作者：平面类、文案类不超过2人；短视频、互动类、广播类不超过3人；影视广告、微电影、动画类、策划案类不超过5人。</p>
      <p>（2）每件作品的指导老师：平面类、文案类不得超过1人；其他类别不得超过2人。</p>
      <p style="color: red;">6、为了使作品资源正常显示或播放，请各位评委使用谷歌Chrome浏览器登录评审系统，或者360浏览器设置成极速模式登录评审系统。</p>
