        <p>1、高校类：全国各类高等院校全日制在校大学生、研究生、学生社团；</p>
        <p>2、专业类：传媒公司、广告（代理）公司、制作公司或广告主；</p>
        <p>3、公众类：热爱公益事业、热心公益传播的组织及个人。</p>
        <p><b>（二）作品形式：</b></p>
