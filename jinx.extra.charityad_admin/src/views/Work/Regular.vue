      <p>第十四条：本次大赛按作品形式类别，设置一等奖、二等奖、三等奖和优秀奖。一等奖为同类别中得分前2%的作品；二等奖为同类别中得分前3%-5%的作品；三等奖为同类别中得分前5%-10%的作品；优秀奖为为同类别中得分前10%-20%的作品。</p>
      <p>第十五条：赛事总奖金30000元，等级奖均可获得对应奖金；一二三等奖奖金比例为3:2:1；最终各奖项奖金额度根据具体获奖数量计算确定。</p>
