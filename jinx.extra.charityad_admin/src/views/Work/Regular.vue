      <p><b>第一章：评奖原则和标准</b></p>
      <p>第一条：评奖坚持公平、公正、公开的原则，严格标准，以质论奖。</p>
      <p>第二条：作品应具有原创性、感染力、实效性及与主题的相关性。具体标准为：</p>
      <p>导向：观点正确，思想性强，有时代特征，符合国家法律、法规、政策要求；</p>
      <p>创意：创意独特，主题鲜明，寓意深刻；</p>
      <p>表达：引人注意，信息传递清晰准确，易认易记，说服力强；</p>
      <p>制作：表现完美，制作严谨，视听语言运用好；</p>
      <p>效果：把握媒体特征，综合效果好。</p>
      <p></p>
      <p><b>第二章：评奖组织</b></p>
      <p>第三条：大赛专家评审委员会将会按照作品分类对本次参赛的作品进行评选。</p>
      <p>第四条：由专家顾问团、学术界专家教授、本省资深评委等组成专家评审委员会。</p>
      <p></p>
      <p><b>第三章：评审</b></p>
      <p>第五条：专家评审委员会对所有参赛的作品进行集体投票方式，对参赛作品进行评审。</p>
      <p>第六条：专家评审委员会实际参加评审的人数必须达到应到会评委人数的三分之二以上，评审结果方为有效。</p>
      <p>第七条：一等奖由专家评审委员会投票并通过核议取得一致意见后最终产生。</p>
      <p>第八条：在评审中，如发现具有抄袭行为的作品（包括意念、图形、方案、）获奖，评委可向专家评委会主任委员提出，经专家评审委员会审定后可取消其资格（该奖项不够时可再增补）。</p>
      <p>第九条：评审中，如评委对评审工作有重要动议，须报主任委员同意，经举手表决达到三分之二以上评委同意后方可执行。</p>
      <p></p>
      <p><b>第四章：评审纪律</b></p>
      <p>第十条：全体评委都应遵循公平公正的原则，不徇私情、不拉票、不诱导其他评委，严格按照评审标准投票。</p>
      <p>第十一条：评审过程中，评委不得翻看、查找作品的作者情况；评审结束后，组委会可对评委公开作者情况，但在正式公示前，任何评委和工作人员均不得公开获奖情况。</p>
      <p>第十二条：评审过程中，当出现有争议的参赛作品时，现场该作品单位评委担任者应自动回避，但有投票权。</p>
      <p>第十三条：评委对评审的有关情况须严格保密，不得泄露作品评议情况，特别是相关争议情况。</p>
